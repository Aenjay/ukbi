                                     <p>Bahasa Indonesia berkedudukan sebagai bahasa nasional dan bahasa resmi negara. Dalam kedudukannya sebagai bahasa nasional, bahasa Indonesia berfungsi sebagai sarana pemersatu berbagai suku bangsa dan sebagai sarana komunikasi antardaerah dan antarbudaya daerah. Sementara itu, dalam kedudukannya sebagai bahasa resmi negara, bahasa Indonesia berfungsi, antara lain, sebagai bahasa resmi kenegaraan, bahasa pengantar pendidikan, bahasa komunikasi tingkat nasional, bahasa media massa, serta bahasa pengembangan dan pemanfaatan ilmu pengetahuan dan teknologi. Dengan kondisi seperti itu, bahasa Indonesia memiliki kedudukan dan peran yang sangat penting dan strategis dalam memfasilitasi proses kemajuan bangsa Indonesia.</p>

                                     <p>Seiring dengan itu, perkembangan bahasa Indonesia harus pula beriringan jalan dengan perkembangan bahasa-bahasa lain di dunia. Perkembangan bahasa Indonesia saat ini telah mencapai era baru dengan dikukuhkannya Undang-Undang Nomor 24 Tahun 2009 tentang Bendera, Bahasa, dan Lambang Negara, serta Lagu Kebangsaan. Di dalam undang-undang tersebut diuraikan peran dan fungsi bahasa Indonesia sebagai bahasa resmi negara. Selain itu, disebutkan pula tentang penggunaan bahasa Indonesia, pengembangan, pembinaan, dan pelindungan bahasa Indonesia. Undang-undang tersebut diperkuat oleh Peraturan Pemerintah Nomor 57 Tahun 2014 tentang Pengembangan, Pembinaan, dan Pelindungan Bahasa dan Sastra, serta Peningkatan Fungsi Bahasa Indonesia.</p>

                                     <p>Dalam pergaulan internasional, Indonesia memiliki posisi tawar yang cukup tinggi mengingat negara Indonesia merupakan destinasi investasi, wisata, dan industri. Posisi tawar tersebut berimplikasi pada penggunaan bahasa Indonesia oleh penutur asing, baik yang berada di Indonesia maupun di luar negeri. Berkembangnnya lembaga penyelenggara BIPA di dalam negeri dan di 64 negara menunjukkan hal itu. Perkembangan itu harus ditangkap sebagai peluang dan sebagai tantangan. Bangsa Indonesia memiliki peluang untuk berdiplomasi dalam berbagai ranah dengan memanfaatkan bahasa Indonesia sehingga negara Indonesia lebih memiliki nilai dalam hubungan regional dan internasional. Kondisi itu menjadi tantangan bagi bangsa Indonesia untuk menunjukkan kualitas bahasa Indonesia sebagai bahasa yang mudah dipelajari dan mampu menjadi bahasa pengetahuan bagi penuturnya.</p>

                                     <p>Untuk menyetarakan bahasa Indonesia agar sejajar dengan bahasa-bahasa besar di dunia, Badan Pengembangan dan Pembinaan Bahasa, Kementerian Pendidikan dan Kebudayaan mengembangkan Uji Kemahiran Berbahasa Indonesia (UKBI). UKBI merupakan tes standar untuk mengetahui kemahiran berbahasa penutur bahasa Indonesia, baik penutur jati maupun penutur asing. Sebagai bangsa yang memiliki bahasa modern yang multifungsi dan memiliki jumlah penutur yang besar, bangsa Indonesia memang harus memiliki sarana evaluasi mutu penggunaan bahasa Indonesia. Tanpa menafikan peran wahana lain, UKBI memiliki fungsi yang amat strategis, tidak hanya untuk meningkatkan kualitas bahasa Indonesia serta penggunaan dan pengajarannya di dalam dan luar negeri, tetapi juga untuk memupuk sikap positif dan rasa bangga masyarakat Indonesia terhadap bahasanya.</p>

                                     <p>Penggunaan Uji Kemahiran Berbahasa Indonesia (UKBI) di masyarakat telah diatur di dalam Peraturan Menteri Pendidikan dan Kebudayaan Nomor 70 Tahun 2016 tentang Standar Kemahiran Berbahasa Indonesia. Hak Cipta UKBI tertuang di dalam Surat Pendaftaran Ciptaan Kementerian Hukum dan HAM Nomor 023993 dan 023994 tertanggal 8 Januari Tahun 2004 dan telah diperbarui pada tahun 2011 atas nama Badan Pengembangan dan Pembinaan Bahasa.</p>

